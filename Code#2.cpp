#include <iostream>
using namespace std;

int main()
{
    int length; //Объявление переменной
    int width; //Объявление другой переменной

    length = 7; //здесь length приисваивается значение 7
    width = 5; //здесь width присваивается значение 5

    cout << "Площадь равна";
    cout << length*width; //здесь выводится 35.

    return 0;
}